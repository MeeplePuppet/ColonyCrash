package com.uprightpath.colonycrash.logic;

/**
 * A grid location.
 */
public class GridLocation {
    /**
     * The X position.
     */
    public int x;

    /**
     * The Y position.
     */
    public int y;

    /**
     * Constructor that sets the x and y.
     *
     * @param x X position.
     * @param y Y position.
     */
    public GridLocation(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
